import sys
from collections import Counter
from typing import List


def get_digit_count(data: List[str]) -> int:
    lengths = []
    for line in data:
        if not line:
            continue
        _, output = line.split("|")
        numbers = output.strip().split()
        lengths.extend(list(map(len, numbers)))
    counter = Counter(lengths)
    return counter[2] + counter[3] + counter[4] + counter[7]


if __name__ == "__main__":
    if len(sys.argv) > 1:
        input_file = sys.argv[1]
        with open(input_file) as fin:
            print(get_digit_count([line.strip() for line in fin]))

